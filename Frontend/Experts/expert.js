Expert1.addEventListener('click', function(event) {

    if(Expert1.classList.contains("btn-default")){
        Expert1.classList.remove("btn-default");
        Expert1.classList.add("btn-primary");
    }
     else if(Expert1.classList.contains("btn-primary")){
        Expert1.classList.add("btn-default");
        Expert1.classList.remove("btn-primary");
     }    
});

